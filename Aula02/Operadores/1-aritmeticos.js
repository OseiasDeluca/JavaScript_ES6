// Módulo (%)
// Operador binário. Retorna o inteiro restante da divisão dos dois operandos.

12 % 5 // Retorna 2.

// Incremento (++) e Decremento (--)
++x 
x++

const a = ++2; // =3
const b = 2++; // =2

--x 
x--

// Negação (-) e Adição (+)
-3
+"3"    // Retorna 3    
+true   // Retorna 1
+false  // Retorna 0
-true   // Retorna -1

// Operadore de exponeciação (**)
2 ** 3 // Retona 8
10 ** -1 // Retorna 0.1

// Operador de agrupamento ()
2 * (3 + 2)