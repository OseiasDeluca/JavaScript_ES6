// Igual (==)
// Retorna verdadeito caso os operadores sejam iguais. 
"3" == var1
3 == '3'

// Não igual (!=)
//Retorna verdadeiro caso os operandos não sejam iguais.
var2 != "3"

// Estritamente igual ou idêntico (===)
// Retorna verdaeiro caso os operandos sejam igual e do mesmo tipo. Veja também Objetc.is e igualdade
3 === var1

// Estritamente não igual (!==)
// Retorna verdadeiro caso os operandos não sejam igual e/ou não sejam do mesmo tipo.
var1 !== "3"


// Maior que (>)
// Retorna verdadeiro caso o operando não sejam iguais e/ou não sejam do mesmo tipo.
var1 !== "3"
3 !== '3'

//Maior ou igual (>=)
// Retorna verdadeiro caso o operando da esquerda seja maior que o da direita.
var2 > var1
"12" > 2

//Menor que (<)
// Retorna verdadeiro caso o operando da esquerda seja menor que o da direita.
var1 < var2
'12' < '2'

// Menor ou igual (<=)
// Retorna verdadeiro caso o operando da esquerda seja menor ou igual que o da direita.
var1 <= var2
var2 <= 5
